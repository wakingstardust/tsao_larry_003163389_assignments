    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        String name = JOptionPane.showInputDialog(this, "Enter Student Name:");
        if(name == null || name.isEmpty()) return;
        
        String nuid = JOptionPane.showInputDialog(this, "Enter NUID:");
        if(nuid == null || nuid.isEmpty()) return;
        
        String username = JOptionPane.showInputDialog(this, "Enter Username:");
        if(username == null || username.isEmpty()) return;
        
        String password = JOptionPane.showInputDialog(this, "Enter Password:");
        if(password == null || password.isEmpty()) return;
        
        Person existingPerson = business.getPersonDirectory().findPersonByNuid(nuid);
        if(existingPerson != null){
            JOptionPane.showMessageDialog(this, "NUID already exists", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Person newPerson = business.getPersonDirectory().newPerson(name);
        newPerson.setNuid(nuid);
        
        StudentProfile newStudent = business.getStudentDirectory().newStudentProfile(newPerson);
        
        business.getUserAccountDirectory().newUserAccount(newStudent, username, password);
        
        JOptionPane.showMessageDialog(this, "Student added successfully");
        populateTable();
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblStudents.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please select a student to delete", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this student?", 
            "Confirm Delete", 
            JOptionPane.YES_NO_OPTION);
        if(confirm != JOptionPane.YES_OPTION) return;

        // Get student name from selected row
        String studentName = (String) tblStudents.getValueAt(selectedRow, 0);

        // Remove from directory
        business.getStudentDirectory().removeStudent(studentName);

        JOptionPane.showMessageDialog(this, "Student deleted successfully");
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

