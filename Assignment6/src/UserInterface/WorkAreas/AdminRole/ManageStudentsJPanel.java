    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0); // Clear existing rows
        model.setColumnCount(0); // Clear columns
        
        // Define columns
        model.addColumn("Name");
        model.addColumn("NUID");
        
        // Get all students from directory
        ArrayList<StudentProfile> students = business.getStudentDirectory().getStudentList();
        
        // Add each student to table using standard for loop
        for(StudentProfile sp : students) {
            Person person = sp.getPerson();
            Object[] row = new Object[2];
            row[0] = person.getPersonId();
            row[1] = person.getNuid() != null ? person.getNuid() : "N/A";
            model.addRow(row);
        }
    }

